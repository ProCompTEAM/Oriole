using System;

namespace Oriole.generic.entity.types
{
	public class BaseType
	{
		public object Body = new object();
		
		public static string Signature
		{
			get { return "base"; }
		}
		
		public BaseType(string value = null)
		{	
			Body = value;
		}
		
		protected void IntBadValue(BaseType t = null)
		{
			if(t == null) t = this;
				
			throw new Exception("Invalid value of type : " + t.Instance + "!");
		}
		
		public virtual string Instance
		{
			get { return Signature; }
		}
		
		public bool InstanceOf(BaseType t)
		{
			return (this.Instance == t.Instance);
		}
	}
}
